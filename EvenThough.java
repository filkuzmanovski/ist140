public class EvenThough {
    public static void main(String[] args) {
        
        // declaring initial variable
       int evenNum = 2;
       
       // while loop for printing even numbers up to 100
       while (evenNum <= 100) 
       {
          System.out.print(" " + evenNum);
          evenNum += 2;
       }
    }
    
}
