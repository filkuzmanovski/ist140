public class MultiTable {

    public static void main(String[] args) {
    	
        // For loop for creating a Multiplication table
        for (int i=1;i<=9;i++){
            for (int j=1;j<=9;j++)
            System.out.print("\t"+i*j);
            
        System.out.println(); 
}
}   
}
